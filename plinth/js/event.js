$('.event-semi-card2').click(function(){
    $('.event-semi-card2>.even-card-coming-soon').css("display", "inline");
    $('.event-semi-card2>img').css("display", "none");
    $('.event-semi-card2>.even-card-name').css("display", "none");
});
$('.event-semi-card2').mouseleave(function(){
    $('.event-semi-card2>.even-card-coming-soon').css("display", "none");
    $('.event-semi-card2>img').css("display", "inline");
    $('.event-semi-card2>.even-card-name').css("display","inline"); 
});


